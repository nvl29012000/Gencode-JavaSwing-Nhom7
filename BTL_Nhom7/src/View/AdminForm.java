    // lọc câu hỏi theo độ khó
    private void getQuestionByDiff(int d){
        listQ = new QuestionDAO().getListQuestion();
        model.setRowCount(0);
        for(Question q : listQ){
            if(d == 0){
                showTableQuestion();
            } else{
                if(q.getLevel() == d){
                    String Lv = "Dễ";
                    if(q.getLevel() == 2){
                        Lv = "Trung bình";
                    }
                    if(q.getLevel() == 3){
                        Lv = "Khó";
                    }
                    model.addRow(new Object[]{
                        q.getQuestion_ID(), q.getQuestion(), Lv, q.getLesson()
                    });
                }
            }
        }
    }   
    // lọc câu hỏi theo lesson
    private void getQuestionByLesson(int l){
        listQ = new QuestionDAO().getListQuestion();
        model.setRowCount(0);
        for(Question q : listQ){
            if(l < 1){
                showTableQuestion();
            } else{
                if(q.getLesson() == l){
                    String Lv = "Dễ";
                    if(q.getLevel() == 2){
                        Lv = "Trung bình";
                    }
                    if(q.getLevel() == 3){
                        Lv = "Khó";
                    }
                    model.addRow(new Object[]{
                        q.getQuestion_ID(), q.getQuestion(), Lv, q.getLesson()
                    });
                }
            }
        }
    }
    // lọc câu hỏi theo độ khó và bài
